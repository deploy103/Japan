function normalizeGloss(gloss) {
  return String(gloss || '')
    .toLowerCase()
    .replace(/\([^)]*\)/g, '')
    .replace(/\bto be\b/g, '')
    .replace(/\bto\b/g, '')
    .replace(/[^a-z0-9\s;-]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function translateGloss(gloss) {
  const normalized = normalizeGloss(gloss);
  if (!normalized) {
    return '';
  }
  if (PHRASE_GLOSS_KO[normalized]) {
    return PHRASE_GLOSS_KO[normalized];
  }
  if (GLOSS_KO[normalized]) {
    return GLOSS_KO[normalized];
  }

  const parts = normalized
    .split(/[;,]/)
    .map((part) => part.trim())
    .filter(Boolean)
    .map((part) => GLOSS_KO[part])
    .filter(Boolean);
  return parts.length ? Array.from(new Set(parts)).join(', ') : '';
}

function translateGlosses(glosses) {
  const translated = [];
  for (const gloss of glosses || []) {
    const value = translateGloss(gloss);
    if (value) {
      translated.push(...value.split(',').map((item) => item.trim()).filter(Boolean));
    }
  }
  return Array.from(new Set(translated));
}

function wordMeaningKo(surface, base) {
  return WORD_KO[surface] || WORD_KO[base] || KATAKANA_KO[surface] || KATAKANA_KO[base] || '';
}

